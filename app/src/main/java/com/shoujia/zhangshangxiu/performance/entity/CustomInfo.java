package com.shoujia.zhangshangxiu.performance.entity;

public class CustomInfo {
    private String customer_name;
    private String mobile;
    private String phone;
    private String card_ye;
    private String cz_ye;
    private String debt;
    private String expend;
    private String customer_id;

    public String getCustomer_name() {
        return customer_name;
    }

    public void setCustomer_name(String customer_name) {
        this.customer_name = customer_name;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getCard_ye() {
        return card_ye;
    }

    public void setCard_ye(String card_ye) {
        this.card_ye = card_ye;
    }

    public String getCz_ye() {
        return cz_ye;
    }

    public void setCz_ye(String cz_ye) {
        this.cz_ye = cz_ye;
    }

    public String getDebt() {
        return debt;
    }

    public void setDebt(String debt) {
        this.debt = debt;
    }

    public String getExpend() {
        return expend;
    }

    public void setExpend(String expend) {
        this.expend = expend;
    }

    public String getCustomer_id() {
        return customer_id;
    }

    public void setCustomer_id(String customer_id) {
        this.customer_id = customer_id;
    }
}
